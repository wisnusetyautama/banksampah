        <h4 id="sejarah"><strong>Sejarah</strong><small> BANK SAMPAH MAKMUR</small></h4>
        <div style="text-align:justify;">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Bapak Sugito, pemrakarsa Bank Sampah di Salatiga yang dinamainya Bank Sampah Makmur Salatiga. Sebelum mendirikan Bank Sampah ini, Bapak Sugito bekerja sebagai pengangkut sampah. Tahun 2012, beliau mengikuti seminar yang diadakan Dinas Cipta Karya Salatiga tentang persoalan sampah. Ia kemudian mengajak tetangganya, Bapak Kumedi Saleh untuk memulai kegiatan ini.<br>
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Dari mereka, pengelolaan sampah dimulai. Sugito bertugas memilah sampah dan membuat kompos. Sementara Kumedi menampung sampah non-organik yang tidak bisa dijual, tetapi masih dapat digunakan, untuk dibuat aneka kreasi. Bapak Sugito awalnya hanya coba-coba membuat kompos hingga akhirnya berhasil. Beliau pun mulai rutin mengelola sampah yang beliau dikumpulkan dari rumah ke rumah. Beliau mulai memilah sampah mana yang dapat dibuat kompos, yang masih laku dijual, dan yang tidak dapat dijual, tetapi dapat didaur ulang. Sisanya, sampah yang tidak bisa dijual dan tidak bisa didaur ulang, dibuang ke tempat pembuangan sementara (TPS).<br>
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Kegiatan tersebut bisa mengurangi pembuangan sampah ke TPS hingg 80%. Sisa 20% itu biasanya hanya sampah sekali pakai seperti sejenis popok dan pembalut. Selain bertugas membuat aneka kreasi dari sampah, Pak Kumedi, yang bertanggung jawab atas divisi sumber daya manusia, juga melatih berbagai pihak yang datang untuk belajar mengelola sampah. Beliau memperlihatkan, sampah yang tidak dapat terurai, seperti styrofoam, kemasan plastik, kaleng, atau botol kaca, dapat disulap menjadi aneka suvenir dan kerajinan tangan kreatif lainnya. Yang selanjutnya membentuk sebuah kelompok khusus bernama <i>Gallery Face Art</i> yang bertugas untuk membuat berbagai macam kerajinan dari barang bekas dan souvenir yang selanjutnya dapat dijual dan menghasilkan uang.<br>
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Perlahan kelompok kecil tersebut mulai membesar dan membetuk diri sebagai Bank Sampah Makmur. Meski bernama Bank Sampah, aktivitas mereka tidak hanya membeli dan mengumpulkan barang bekas dari warga yang menjual sampah. Kompos yang dibuat oleh unit pengolahan kompos pun mulai dimanfaatkan ibu-ibu yang mendapat dana dari program Kawasan Rumah Pangan Lestari dari Badan Pemberdayaan Masyarakat Desa. Pemuda Karang Taruna setempat pun mulai tergerak untuk mulai peduli dan melihat bahwa sampah bisa menghasilkan.</div>


